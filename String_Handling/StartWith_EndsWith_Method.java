package String_Handling;

public class StartWith_EndsWith_Method {
    public static void main(String[] args) {
        String str="Rushikesh";
//        This method return the Boolean Value if Start character is matches
        System.out.println(str.startsWith("R"));//true
        
//        This method return the Boolean Value if End Character is Matches
        System.out.println(str.endsWith("s"));//false
    }
}
